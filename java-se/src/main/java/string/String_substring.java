public class String_substring {
    public static void main(String[] args) {
        String s = "helloworld";
        System.out.println(s.substring(0));
        System.out.println(s.substring(1));
        System.out.println(s.substring(0, 0));
        System.out.println(s.substring(1, 3));
        System.out.println(s.substring(1));
        System.out.println(s.substring(1, 3));
    }
}
